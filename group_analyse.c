bool people[560]; // 560 whether a person has been added
int parent[560]; // can reuse any other array (don't have to be clear) to save memory usage
int size[560]; // can reuse any other array (don't have to be clear) to save memory usage
int ng, lg;
int ans_djs[2];

// for faster clear()
int cnt_djs;
int used_djs[560]; // can reuse any other array (don't have to be clear) to save memory usage


// functions

int findSet(int i) {

    // path compression
    if ( i != parent[i] ) parent[i] = findSet(parent[i]);
    else return i;

    return parent[i];
}

void linkSet(int i, int j) {
    int si = findSet(i);
    int sj = findSet(j);

    if (si != sj) {
        if (size[si] >= size[sj]) {
            parent[sj] = si;
            size[si] += size[sj];

            if (lg < size[si]) lg = size[si];

        } else {
            parent[si] = sj;
            size[sj] += size[si];

            if (lg < size[sj]) lg = size[sj];
        }
        ng--;
    }

    return;
}

void makeSet(int i, int j) { 

    if (!people[i] && !people[j]) {
        
        parent[i] = i;
        size[i] = 2;
        people[i] = true;

        parent[j] = i;
        people[j] = true;

        ng++;
        if (lg < 2) lg = 2;

	used_djs[cnt_djs++] = i;
	used_djs[cnt_djs++] = j;

    } else if (!people[i]) {

        parent[i] = findSet(j);
        size[parent[i]]++;
        people[i] = true;

        if (lg < size[parent[i]]) lg = size[parent[i]];

	used_djs[cnt_djs++] = i;

    } else if (!people[j]) {

        parent[j] = findSet(i);
        size[parent[j]]++;
        people[j] = true;

	used_djs[cnt_djs++] = j;
        
        if (lg < size[parent[j]]) lg = size[parent[j]];

    } else {
        linkSet(i, j);
    }

    return;
}

void clear() {
    for (int i = 0; i < cnt_djs; i++)
	people[used_djs[i]] = 0;
}


